function setup () {
    // buat wrapper jika belum ada
    let wrapper = document.getElementById('canvas-box');
    if (!wrapper) {
        wrapper = document.createElement('div');
        wrapper.id = 'canvas-box';
        document.body.appendChild(wrapper);
    }

    // buat canvas dan pasang ke wrapper
    const cnv = createCanvas(600, 600);
    cnv.parent('canvas-box');

    // styling lewat JS agar tidak perlu ubah HTML/CSS terpisah
    document.documentElement.style.height = '100%';
    document.body.style.minHeight = '100vh';
    document.body.style.margin = '0';
    document.body.style.display = 'flex';
    document.body.style.justifyContent = 'center';
    document.body.style.alignItems = 'center';

    wrapper.style.display = 'inline-flex';
    wrapper.style.justifyContent = 'center';
    wrapper.style.alignItems = 'center';
    wrapper.style.padding = '12px';
    wrapper.style.border = '2px solid #333';
    wrapper.style.borderRadius = '8px';
    wrapper.style.background = '#fff';
    wrapper.style.boxShadow = '0 4px 12px rgba(0,0,0,0.15)';

    // hilangkan whitespace default pada elemen canvas
    cnv.elt.style.display = 'block';

    background (49, 21, 80);
}

